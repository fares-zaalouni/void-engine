VulkanRenderer::VulkanRenderer() :
    _vkContext(),
    _deviceManager(_vkContext),
    _swapChainManager(_vkContext)
{
}
void VulkanRenderer::Init(Window* window)
{
    
    _deviceManager.CreateInstance();
    _vkContext.CreateSurface(window);
    _deviceManager.PickPhysicalDevice();
    _deviceManager.CreateLogicalDevice();
    _swapChainManager.CreateSwapChain(window->getGLFWwindow());
    _swapChainManager.CreateImageViews();
    
    CreateRenderPass();
    CreateGraphicsPipeline();
    _swapChainManager.CreateFramebuffers();
    CreateCommandPool();
    CreateCommandBuffer();
    CreateSyncObjects();

}








std::vector<char> VulkanRenderer::ReadFile(const std::string& filename)
{
    std::ifstream file(filename, std::ios::ate | std::ios::binary);
    if (!file.is_open()) {
        std::cerr << "Failed to open file: " << filename << std::endl;
        throw std::runtime_error("failed to open file!");
    }

    size_t fileSize = (size_t)file.tellg();
    std::vector<char> buffer(fileSize);
    file.seekg(0);
    file.read(buffer.data(), fileSize);
    file.close();
    return buffer;
}

